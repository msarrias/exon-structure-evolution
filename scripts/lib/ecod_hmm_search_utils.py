def read_pkl_file(
        file_path: str,
) -> dict:
    with open(file_path, 'rb') as handle:
        read_file = pickle.load(handle)
    return read_file


def dump_sequence_dictionary_into_fasta_file(
        out_file_path: Path,
        sequence_dictionary: dict
) -> None:
    with open(out_file_path, "w") as handle:
        for annotation_id, annotation_sequence in sequence_dictionary.items():
            record = SeqRecord(
                Seq(annotation_sequence),
                id=str(annotation_id),
                description=''
            )
            SeqIO.write(record, handle, "fasta")


def run_hmmsearch(
        out_hmmsearch_path: str,
        hmmsearch_profile_path: str,
        fasta_file_path: str,
        evalue: float
) -> None:
    hmmsearch_command = [
        'hmmsearch',
        '--domtblout', out_hmmsearch_path,
        '-E', str(evalue),
        hmmsearch_profile_path,
        fasta_file_path
    ]
    subprocess.run(
        hmmsearch_command,
        stdout=subprocess.DEVNULL,
        stderr=subprocess.STDOUT
    )


def collect_hmm_matches(
        hmmsearch_file_path
):
    """
    parse_hmmr_output_file parses the output file of hmmsearch and returns
    a list of tuples.
    :param hmmsearch_file_path:
    :return:
    """
    # In a dombtblout output file generated by HMMER, each line represents a significant hit to a profile
    # Hidden Markov Model (HMM) from a sequence database.
    # The columns in a dombtblout file contain the following information:
    #  1. target name: the name of the sequence database entry that corresponds to the hit
    #  2. target accession: the accession of the query sequence
    #  3. tlen: the length of the target sequence
    #  4. query name: the name of the query sequence
    #  5. accession: the accession of the query sequence
    #  6. qlen: the length of the query sequence
    #  7. E-value: the expectation value (E-value) of the match
    #  8. score: the score of the match
    #  9. bias: the bias of the match
    # 10. # of domains: the number of domains in the match
    # 11. of: the number of the match in the query sequence
    # 12. c-Evalue: the conditional E-value (E-value of the match given the hit is from the same model)
    # 13. i-Evalue: the independent E-value (E-value of the match given the hit is from a different model)
    # 14. score: the score of the match
    # 15. bias: the bias of the match
    # 16. from: the start position of the match in the query sequence (1 based)
    # 17. to: the end position of the match in the query sequence (1 based)
    # 18. from: the start position of the match in the target sequence (1 based)
    # 19. to: the end position of the match in the target sequence (1 based)
    # 20. from: the start position of the envelope in the query sequence (1 based)
    # 21. to: the end position of the envelope in the query sequence (1 based)
    # 22. from: the start position of the envelope in the target sequence (1 based)
    # 23. to: the end position of the envelope in the target sequence (1 based)
    # 24. acc: the accuracy of the match
    # 25. description of target sequence: the description of the target sequence
    # 26. description of query sequence: the description of the query sequence
    # 27. # of envs: the number of envelopes in the match
    # 28. # of hmm: the number of HMMs in the match

    #  3. query name: the name of the query sequence
    matches_list = []
    with open(hmmsearch_file_path, 'r') as file_handler:
        for line in file_handler:
            if line.startswith('#'):
                continue
            fields_list = line.strip().split()

            (
                t_len,
                query_name,
                query_accession,
                qlen,
                seq_evalue,
                seq_score,
                seq_bias,
                domain_num,
                domain_total,
                domain_c_evalue,
                domain_i_evalue,
                domain_score,
                domain_bias,
                hmm_from,
                hmm_to,
                ali_from,
                ali_to,
                env_from,
                env_to,
                acc,
                target_description
            ) = fields_list[2:]
            # HMMER coordinates are 1-based and inclusive
            # we need to subtract 1 from the start position
            # to get the 0-based start position
            # Target: e.g., cds/mrna
            ali_from, ali_to = int(ali_from) - 1, int(ali_to)
            # Query: hmm profile, e.g., F group
            hmm_from, hmm_to = int(hmm_from) - 1, int(hmm_to)
            matches_list.append(
                [
                    #  the target_name is of the form gene_id-transcript_id-cds_id/gene_id-transcript_id-
                    *list([field for field in fields_list[0].rsplit('-')]),
                    t_len,
                    query_name,
                    query_accession,
                    qlen,
                    seq_evalue,
                    seq_score,
                    seq_bias,
                    domain_num,
                    domain_total,
                    domain_c_evalue,
                    domain_i_evalue,
                    domain_score,
                    domain_bias,
                    hmm_from,
                    hmm_to,
                    ali_from,
                    ali_to,
                    env_from,
                    env_to,
                    acc,
                    target_description,
                    round((hmm_to - hmm_from) / int(qlen), 3),
                    round((ali_to - ali_from) / int(t_len), 3)
                ]
            )
    return matches_list


def fetch_and_dump_data(
        file_path: Path,
        query_function: callable,
        key_format: str,
        protein_db_path: Path,
        timeout_db: int
) -> None:
    if not os.path.exists(protein_db_path):
        raise FileNotFoundError(
            f'Protein database not found: {protein_db_path},'
            f' use prot_sql_utils.py to generate it'
        )
    if not os.path.exists(file_path):
        results = query_function(
            protein_db_path,
            timeout_db
        )
        sequence_dictionary = {
            key_format.format(*sequence): (
                sequence[-1][:-1]
                if sequence[-1][-1] == '*'  # remove stop codon
                else sequence[-1]
            )
            for sequence in results if sequence[-1]
            # remove stop codon
        }
        dump_sequence_dictionary_into_fasta_file(
            out_file_path=file_path,
            sequence_dictionary=sequence_dictionary
        )


