def get_representative_matches_mrna_id_set(
        matches_gene_mapping_ranges_list: list,
        overlapping_threshold: float,
):
    hmm_gene_f_group_dict = defaultdict(lambda: defaultdict(list))
    for record in matches_gene_mapping_ranges_list:
        (hmm_match_id,
         gene_id,
         f_group,
         x_group_name,
         x_group_id,
         hmm_start,
         hmm_end,
         align_start,
         align_end,
         evalue,
         symmetry_score,
         gene_mapping_coords
         ) = record
        hmm_gene_f_group_dict[gene_id][x_group_id].append((
            record,
            hmm_match_id,
            P.open(hmm_start, hmm_end),
            P.open(align_start, align_end),
            evalue,
            get_p_coordinates_list_union(
                p_coordinates_list=get_coordinates_list(
                    gene_mapping_coords_str=gene_mapping_coords
                ))
        )
        )
    representative_matches_id_list = set()
    tuples_to_insert = []
    for gene_id, f_group_dictionary in hmm_gene_f_group_dict.items():
        for x_group_id, annotation_list in f_group_dictionary.items():
            if len(annotation_list) > 1:
                overlapping_clusters = get_overlapping_clusters(
                    target_coordinates_set=set([
                        (prot_gene_coordinate, (annotation_id, evalue))
                        for _, annotation_id, _, _, evalue, prot_gene_coordinate in annotation_list]),
                    threshold=overlapping_threshold
                )
                representative_matches_id_list.update([
                    min(annotation, key=lambda x: x[-1][1])[-1][0] for annotation in overlapping_clusters
                ])
                tuples_to_insert.extend([record
                                         for record, match_id, *_ in annotation_list
                                         if match_id in representative_matches_id_list
                                         ])
            else:
                tuples_to_insert.append(annotation_list[0][0])
    return tuples_to_insert


def get_candidate_cds_coordinates(
        gene_id: str,
        gene_hierarchy_dictionary: dict,
        min_exon_length: int,
) -> dict:
    """
    get_candidate_cds_coordinates is a function that given a gene_id,
    collects all the CDS coordinates with a length greater than the
    minimum exon length across all transcript.
    If there are overlapping CDS coordinates, they are resolved
    according to the following criteria:

    * If they overlap by more than the overlapping threshold
      of both CDS lengths the one with the highest overlapping
      percentage is selected.
    * Both CDSs are selected otherwise

    The rationale behind choosing the interval with the higher percentage
    of overlap is that it will favor the selection of shorter intervals
    reducing the exclusion of short duplications due to coverage thresholds.

    :param gene_id: gene identifier
    :param gene_hierarchy_dictionary: gene hierarchy dictionary
    :param min_exon_length: minimum exon length
    :return: list of representative CDS coordinates across all transcripts
    """
    # collect all CDS coordinates and frames across all transcripts
    # we are interested in the frames to account for the unlikely event
    # that two CDS with same coordinates in different transcripts
    # have different frames
    cds_coordinates_and_frames: list[tuple[P.Interval, str]] = list(
        set(
            (coordinate, annotation_structure['frame'])
            for mrna_annotation in gene_hierarchy_dictionary[gene_id]['mRNAs'].values()
            for annotation_structure in mrna_annotation['structure']
            for coordinate in (annotation_structure['coordinate'],)
            if (
                    annotation_structure['type'] == 'CDS'
                    and (coordinate.upper - coordinate.lower) >= min_exon_length
            )
        )
    )
    if cds_coordinates_and_frames:
        representative_cds_frame_dictionary = dict()
        for cds_coordinate, frame in cds_coordinates_and_frames:
            if cds_coordinate in representative_cds_frame_dictionary:
                representative_cds_frame_dictionary[cds_coordinate] += f'_{str(frame)}'
            else:
                representative_cds_frame_dictionary[cds_coordinate] = str(frame)
        sorted_cds_coordinates_list: list[P.Interval] = sorted(
            [coordinate for coordinate, _ in cds_coordinates_and_frames],
            key=lambda coordinate: (coordinate.lower, coordinate.upper),
        )
        return dict(
            candidates_cds_coordinates=resolve_overlaps_between_coordinates(
                sorted_cds_coordinates=sorted_cds_coordinates_list
            ),
            cds_frame_dict=representative_cds_frame_dictionary,
        )
    return dict()


def resolve_overlaps_between_coordinates(
        sorted_cds_coordinates: list[P.Interval],
        overlapping_threshold: float = 0.9,
) -> list[P.Interval]:
    """
    resolve_overlaps_between_coordinates is a recursive function that given
    a list of coordinates, resolves overlaps between them.
    :param sorted_cds_coordinates: list of unique and sorted coordinates.
    :param overlapping_threshold: percentage of overlap
    :return: list of coordinates without "overlaps"
    """
    new_list = list()
    # We only process the first pair of overlapping intervals since
    # the resolved overlap could also overlap with the next interval in the list.
    intv_i, intv_j = get_first_overlapping_intervals(
        sorted_intervals=sorted_cds_coordinates,
        overlapping_threshold=overlapping_threshold
    )
    if all((intv_i, intv_j)):
        candidate = get_single_candidate_cds_coordinate(
            intv_i=intv_i,
            intv_j=intv_j
        )
        # Note: new_list is populated through enumeration
        # of 'sorted_cds_coordinates', so it will also be sorted
        # according to the same criteria used for 'sorted_cds_coordinates'.
        for idx, cds_coordinate in enumerate(sorted_cds_coordinates):
            if cds_coordinate != intv_i:
                new_list.append(cds_coordinate)
            else:
                new_list.append(candidate)
                new_list.extend(sorted_cds_coordinates[idx + 2:])
                return resolve_overlaps_between_coordinates(
                    sorted_cds_coordinates=new_list
                )
    return sorted_cds_coordinates


def get_first_overlapping_intervals(
        sorted_intervals: list[P.Interval],
        overlapping_threshold: float,
) -> Union[tuple[P.Interval, P.Interval], tuple[None, None]]:
    """
    Given a list of intervals, get_first_overlapping_intervals returns
    the first consecutive interval tuples with overlapping pairs.
    :param sorted_intervals: list of intervals
    :param overlapping_threshold: percentage of overlap
    :return: overlapping intervals pair
    """
    first_overlap_index = 0
    while first_overlap_index < len(sorted_intervals) - 1:
        current_interval = sorted_intervals[first_overlap_index]
        next_interval = sorted_intervals[first_overlap_index + 1]
        if min_perc_overlap(
                intv_i=current_interval,
                intv_j=next_interval) >= overlapping_threshold:
            return current_interval, next_interval
        first_overlap_index += 1
    return None, None


def get_single_candidate_cds_coordinate(
        intv_i: P.Interval,
        intv_j: P.Interval,
) -> P.Interval:
    """
    Given two sorted intervals this function returns the interval with
    the largerst percentage of overlap.
    Assumption: intv_i =/= intv_j and they are sorted such that
    intv_i.lower < intv_j.lower.
    param intv_i: the first interval
    param intv_j: the second interval
    Returns: P.interval
    """
    if (
            get_overlap_percentage(
                intv_i=intv_i,
                intv_j=intv_j
            ) >
            get_overlap_percentage(
                intv_i=intv_j,
                intv_j=intv_i
            )
    ):
        return intv_j
    return intv_i


def get_overlap_percentage(
        intv_i: P.Interval,
        intv_j: P.Interval,
) -> float:
    """
    Given two intervals, the function get_overlap_percentage returns the percentage
    of the overlapping region relative to an interval j.
    """
    intersection = intv_i & intv_j
    if intersection:
        return (intersection.upper - intersection.lower) / (intv_j.upper - intv_j.lower)
    return 0.0


def get_domain_matches(
    coordinates_list,
    coordinate_i,
    threshold
):
    return [
        (match_id, type_match, coordinate, coordinate_i)
        for match_id, type_match, coordinate in coordinates_list
        if min_perc_overlap(coordinate_i, coordinate) >= threshold
    ]


def get_sub_domain_matches(
    coordinates_list,
    coordinate_i,
    threshold
):
    return [
        coordinate
        for coordinate in coordinates_list
        if intervals_percentage_overlap(coordinate_i, coordinate) >= threshold
    ]


def get_mrna_matches_domain_subdomain_categories(
        query_mrna_hmm_matches_dictionary: dict,
        genes_dictionary: dict,
        exonize_events_dictionary: dict,
        gene_hierarchy_dictionary: dict,
        overlapping_threshold: float = 0.9,
        symmetry_score_threshold: float = 0.8,
):
    # FOR UPDATING THE x_group_filtered_ECOD_hmm_mRNA TABLE
    group_missing_symmetry_set = set()
    tuples_to_insert = list()
    for gene_id, mrna_hmm_matches in query_mrna_hmm_matches_dictionary.items():
        ensembl_gene_idef = f"gene:{gene_id}"
        coords, chromosome, strand = genes_dictionary[ensembl_gene_idef]
        cds_coordinates_list = get_candidate_cds_coordinates(
            gene_id=ensembl_gene_idef,
            gene_hierarchy_dictionary=gene_hierarchy_dictionary,
            min_exon_length=0
        )
        cds_coordinates_list = set([
            P.open(cds_coordinate.lower - coords.lower, cds_coordinate.upper - coords.lower)
            for cds_coordinate in cds_coordinates_list['candidates_cds_coordinates']
        ])
        gene_events_coordinates = set([
            coordinate
            for events_list in exonize_events_dictionary[gene_id].values()
            for coordinate, *_, in events_list
        ])
        cds_coordinates_list = cds_coordinates_list.union(gene_events_coordinates)
        for full_domain_match in mrna_hmm_matches:
            mrna_match_id, *_, x_group_id, symmetry_score, _, gene_mapping_coord = full_domain_match
            is_within_domain_boundary, is_full_domain_exon, is_subdomain_exon = 0, 0, 0
            cds_domain_matches = [
                coordinate
                for coordinate in cds_coordinates_list
                if min_perc_overlap(gene_mapping_coord, coordinate) >= overlapping_threshold
            ]
            if cds_domain_matches:
                is_full_domain_exon += len(set(cds_domain_matches))
            else:
                cds_sub_domain_matches = get_sub_domain_matches(
                    coordinates_list=cds_coordinates_list,
                    coordinate_i=gene_mapping_coord,
                    threshold=overlapping_threshold
                )
                if cds_sub_domain_matches:
                    n_cdss_matching = len(set(cds_sub_domain_matches))
                    is_within_domain_boundary += n_cdss_matching
                    if symmetry_score is not None and symmetry_score >= symmetry_score_threshold:
                        if symmetry_score >= symmetry_score_threshold:
                            is_within_domain_boundary -= n_cdss_matching
                            is_subdomain_exon += n_cdss_matching
                    else:
                        group_missing_symmetry_set.add(x_group_id)
            tuples_to_insert.append((
                is_within_domain_boundary,
                is_full_domain_exon,
                is_subdomain_exon,
                mrna_match_id
            ))
    return tuples_to_insert, group_missing_symmetry_set


def get_events_domain_subdomain_categories(
    exonize_events_dictionary: dict,
    hmm_mrna_cds_matches_dict: dict,
    gene_hierarchy_dictionary: dict,
    overlapping_threshold: float = 0.9,
    symmetry_score_threshold: float = 0.2,
):
    # FOR UPDATING THE Events TABLE
    x_group_ids_missing_symmetry_score_set = set()
    records_to_insert_matches_events = list()
    records_to_insert = list()
    for gene_id, expansion_dictionary in exonize_events_dictionary.items():
        trimmed_gene_id = gene_id.rsplit(':')[1]
        gene_start = gene_hierarchy_dictionary[gene_id]['coordinate'].lower
        if trimmed_gene_id in hmm_mrna_cds_matches_dict:
            mrna_hmm_matches = hmm_mrna_cds_matches_dict[trimmed_gene_id]
            for expansion_id, events_list in expansion_dictionary.items():
                for event in events_list:
                    event_coordinate, event_type = event
                    is_within_domain_boundary, is_full_domain_exon, is_subdomain_exon = 0, 0, 0
                    full_domain_matches_coordinates = get_domain_matches(
                        coordinates_list=[
                            (match_id, match_type, gene_mapping_coord)
                            for xid, coordinates_list in mrna_hmm_matches.items()
                            for match_type, match_id, gene_mapping_coord, _ in coordinates_list
                        ],
                        coordinate_i=event_coordinate,
                        threshold=overlapping_threshold
                    )
                    if full_domain_matches_coordinates:
                        is_full_domain_exon = len(full_domain_matches_coordinates)
                        records_to_insert_matches_events.extend([
                            (type_match,
                             'FullDomainExon',
                             match_id,
                             gene_id,
                             event_coords.lower,
                             event_coords.upper)
                            for (match_id,
                                 type_match,
                                 match_coords,
                                 event_coords) in full_domain_matches_coordinates
                        ])
                    else:
                        subdomain_matches = [
                            (x_group_id,
                             match_id,
                             match_type,
                             gene_mapping_coord,
                             event_coordinate,
                             symmetry_score)
                            for x_group_id, coordinates_list in mrna_hmm_matches.items()
                            for match_type, match_id, gene_mapping_coord, symmetry_score in coordinates_list
                            if intervals_percentage_overlap(
                                gene_mapping_coord, event_coordinate
                            ) >= overlapping_threshold
                        ]
                        if subdomain_matches:
                            for (x_group_id,
                                 match_id,
                                 match_type,
                                 gene_mapping_coord,
                                 event_coordinate,
                                 symmetry_score) in subdomain_matches:
                                is_within_domain_boundary += 1
                                if symmetry_score:
                                    if symmetry_score >= symmetry_score_threshold:
                                        records_to_insert_matches_events.append((
                                            match_type,
                                            'SubDomainExon',
                                            match_id,
                                            trimmed_gene_id,
                                            event_coordinate.lower,
                                            event_coordinate.upper
                                        ))
                                        is_subdomain_exon += 1
                                        is_within_domain_boundary -= 1
                                    else:
                                        records_to_insert_matches_events.append((
                                            match_type,
                                            'WithinDomainBoundary',
                                            match_id,
                                            trimmed_gene_id,
                                            event_coordinate.lower,
                                            event_coordinate.upper
                                        ))
                                else:
                                    x_group_ids_missing_symmetry_score_set.add(x_group_id)
                    records_to_insert.append((
                        is_within_domain_boundary,
                        is_full_domain_exon,
                        is_subdomain_exon,
                        gene_id,
                        event_coordinate.lower + gene_start,
                        event_coordinate.upper + gene_start
                    ))
        else:
            for expansion_id, events_list in expansion_dictionary.items():
                for event in events_list:
                    event_coordinate, event_type = event
                    records_to_insert.append((
                        0,
                        0,
                        0,
                        gene_id,
                        event_coordinate.lower + gene_start,
                        event_coordinate.upper + gene_start
                    ))
    return records_to_insert, records_to_insert_matches_events, x_group_ids_missing_symmetry_score_set
