def get_contingency_tables(
        ecod_db_path: Path,
        exonize_results_db_path: Path
) -> (list, list):
    human_genes = query_genes(db_path=exonize_results_db_path)
    (genes_with_exon_duplication_exonize,
     full_domain_exon_duplication_mrna_cds_search,
     genes_with_sub_domain_exonize,
     genes_with_full_domain_mrna_hmm,
     genes_with_full_domain_cds_hmm,
     genes_with_sub_domain_hmm,
     genes_within_domain_boundary_hmm
     ) = query_ecod_subdomain_domain_counts(
        db_path=ecod_db_path,
        hmm_mrna_table_name="x_group_filtered_ECOD_hmm_mRNA",
        hmm_cds_table_name="x_group_filtered_ECOD_hmm_CDSs",
        expansions_table_name="Expansions_full"
    )
    # Because of the overlapping threshold criteria there are some envents that can map to the same
    # match in thr Hmm search, as both, subdomain and full domain. We exclude those events since
    # they are already classified as full-domain, and this could indicate that the alignment
    # was close to our cut-off threshold.
    mrna_match_mapping_to_sub_doman_domain = [gene for gene in genes_with_sub_domain_exonize
                                              if gene not in genes_with_sub_domain_hmm]
    if mrna_match_mapping_to_sub_doman_domain:
        genes_with_sub_domain_exonize = set([gene for gene in genes_with_sub_domain_exonize
                                             if gene not in mrna_match_mapping_to_sub_doman_domain])
    # # Calculate counts
    # Genes with full domain found by HMM in mRNA and CDS
    genes_with_full_domain_hmm = genes_with_full_domain_mrna_hmm.union(genes_with_full_domain_cds_hmm)
    n_genes_with_full_domain_exon = len(genes_with_full_domain_hmm)
    # Genes with full domain exon and exon duplication (both traits)
    gene_ids_with_both_traits = genes_with_full_domain_hmm.intersection(genes_with_exon_duplication_exonize)
    n_genes_with_both_traits_fd = len(gene_ids_with_both_traits)
    # Difference to find genes with exon duplication but no full domain exon
    genes_with_exon_duplication_only_fd = genes_with_exon_duplication_exonize.difference(genes_with_full_domain_hmm)
    n_genes_with_exon_duplication_only_fd = len(genes_with_exon_duplication_only_fd)
    # Count of genes without full domain exon
    n_genes_with_no_full_domain_exon = len(human_genes) - n_genes_with_full_domain_exon
    # Odds ratio calculation: odds of a gene having an exon duplication given it has a full domain exon
    # Contingency table A: FULL-DOMAIN EXON
    contingency_table_A = [
        [n_genes_with_both_traits_fd, n_genes_with_full_domain_exon - n_genes_with_both_traits_fd],
        [n_genes_with_exon_duplication_only_fd, n_genes_with_no_full_domain_exon - n_genes_with_exon_duplication_only_fd]
    ]
    n_genes_with_both_traits_sd = len(genes_with_sub_domain_exonize.intersection(genes_with_sub_domain_hmm))
    n_genes_with_sub_domain_exon = len(genes_with_sub_domain_hmm)
    n_genes_with_exon_duplication_only_sd = len(genes_with_exon_duplication_exonize.difference(genes_with_sub_domain_hmm))
    n_genes_with_no_sub_domain_exon = len(human_genes) - n_genes_with_sub_domain_exon
    # Contingency table B: SUB-DOMAIN EXON
    contingency_table_B = [
        [n_genes_with_both_traits_sd, n_genes_with_sub_domain_exon - n_genes_with_both_traits_sd],
        [n_genes_with_exon_duplication_only_sd, n_genes_with_no_sub_domain_exon - n_genes_with_exon_duplication_only_sd]
    ]
    # Odds C:
    odds_counts_C = [
        len(full_domain_exon_duplication_mrna_cds_search),
        len(gene_ids_with_both_traits) - len(full_domain_exon_duplication_mrna_cds_search)
    ]
    return contingency_table_A, contingency_table_B, odds_counts_C


def get_overlapping_clusters(
        target_coordinates_set: set,
        threshold: float,
) -> list[list[tuple[P.Interval, Union[float, tuple]]]]:
    """
    Get overlapping clusters of target coordinates.
    """
    processed_intervals = set()
    overlapping_clusters = []
    # Sort the coordinates by their lower bound for efficient processing
    sorted_coordinates = sorted(target_coordinates_set, key=lambda x: x[0].lower)
    for target_coordinate, evalue in sorted_coordinates:
        if target_coordinate not in processed_intervals:
            # Create a cluster for the current coordinate
            cluster = [(target_coordinate, evalue)]
            # Find overlapping coordinates and add to the cluster
            for other_coordinate, other_evalue in sorted_coordinates:
                if (
                        target_coordinate != other_coordinate and
                        (min_perc_overlap(
                            intv_i=target_coordinate,
                            intv_j=other_coordinate,
                        ) >= threshold or target_coordinate.contains(other_coordinate))
                ):
                    cluster.append((other_coordinate, other_evalue))
                    processed_intervals.add(other_coordinate)
            overlapping_clusters.append(cluster)
            processed_intervals.add(target_coordinate)
    overlapping_clusters.sort(key=len, reverse=True)
    return overlapping_clusters


def get_candidate_cds(
        cds_records_dictionary: dict,
        cds_overlapping_cluster: list
):
    cdss_matches_counts = [(cds_coordinate,
                            sum([len(matches_list) for x_group_id, matches_list in cds_records_dictionary.items()]))
                           for cds_coordinate in cds_overlapping_cluster]
    candidate_cds_coordinate, _ = max(cdss_matches_counts, key=lambda x: x[1])
    return candidate_cds_coordinate


def filter_cds_records_for_gene(
        cds_records_dictionary: dict,
        threshold: float,
) -> list:
    filtered_records = []
    coordinates_set = set([(cds_coordinate, None) for cds_coordinate in cds_records_dictionary.keys()])
    overlapping_clusters_list = get_overlapping_clusters(
        target_coordinates_set=coordinates_set,
        threshold=threshold
    )
    for cds_overlapping_cluster in overlapping_clusters_list:
        if len(cds_overlapping_cluster) > 1:
            cds_keeper = get_candidate_cds(
                cds_records_dictionary=cds_records_dictionary,
                cds_overlapping_cluster=cds_overlapping_cluster
            )
        else:
            cds_keeper, _ = cds_overlapping_cluster[0]
        for x_group_name, matches_list in cds_records_dictionary[cds_keeper].items():
            if len(matches_list) > 1:
                filtered_matches = filter_matches_for_x_group(
                    matches_list=matches_list,
                    threshold=threshold
                )
                filtered_records.extend(filtered_matches)
            else:
                filtered_records.extend(matches_list)
    return filtered_records


def get_filtered_ecod_hmm_cds_records(
        hmm_hits_dictionary: dict,
        threshold: float
) -> list:
    filtered_records_list = set()
    for gene_id, cds_records_dictionary in hmm_hits_dictionary.items():
        filtered_records = filter_cds_records_for_gene(
            cds_records_dictionary=cds_records_dictionary,
            threshold=threshold
        )
        filtered_records_list.update(filtered_records)
    return list(filtered_records_list)


def filter_matches_for_x_group(
        matches_list: list,
        threshold: float,
) -> list:
    x_group_matches_list = set([
        (P.open(align_start, align_end), (annot_id, evalue))
        for annot_id, *_, align_start, align_end, evalue, _, _ in matches_list
    ])
    overlapping_clusters = get_overlapping_clusters(
        target_coordinates_set=x_group_matches_list,
        threshold=threshold
    )
    filtered_matches = []
    for cluster in overlapping_clusters:
        if len(cluster) > 1:
            min_evalue_match = min(cluster, key=lambda x: x[1][-1])
            annot_id = min_evalue_match[1][0]
        else:
            _, (annot_id, _) = cluster[0]
        filtered_matches.extend([match for match in matches_list if match[0] == annot_id])
    return filtered_matches


def intervals_percentage_overlap(
        intv_i: P.Interval,
        intv_j: P.Interval,
        ) -> float:
    if intv_i.overlaps(intv_j):
        intersection_span = get_interval_length(intv_i.intersection(intv_j))
        return round(intersection_span / get_interval_length(intv_j), 3)
    return 0.0


