def min_perc_overlap(
        intv_i: P.Interval,
        intv_j: P.Interval,
) -> float:
    """
    Given two intervals, the function returns the percentage of the overlapping
    region relative to the longest interval. The percentage overlap of the shortest
    interval will always be greater or equal than that of the longest interval.
    :param intv_i:
    :param intv_j:
    :return:
    """
    if intv_i.overlaps(intv_j):
        intersection_span = get_interval_length(intv_i.intersection(intv_j))
        longest_length = max(get_interval_length(intv_i), get_interval_length(intv_j))
        return round(intersection_span / longest_length, 3)
    return 0.0


def parse_txt_file(
        txt_file_path: Path,
) -> list:
    records = []
    with open(txt_file_path, 'r') as file:
        for line in file:
            # Skip commented lines
            if line.startswith('#'):
                continue
            records.append(line.strip().split('\t'))
    return records


def verify_f_group_matches_are_non_overlapping(
    hmm_mrna_matches_dict: dict,
    overlapping_threshold: float
) -> bool:
    overlapping_matches_for_f_group = False
    for gene_id, transcript_dictionary in hmm_mrna_matches_dict.items():
        for transcript_id, f_group_dictionary in transcript_dictionary.items():
            for f_group, matches_list in f_group_dictionary.items():
                if len(matches_list) > 1:
                    overlapping_list = [
                        [intv_i, intv_j]
                        for _, intv_i in matches_list
                        for _, intv_j in matches_list
                        if intv_i != intv_j and
                        intv_i.overlaps(intv_j) and
                        min_perc_overlap(intv_i, intv_j) >= overlapping_threshold
                    ]
                    if overlapping_list:
                        overlapping_matches_for_f_group = True
    return overlapping_matches_for_f_group


def get_interval_length(
        interval: P.Interval,
):
    return sum(intv.upper - intv.lower for intv in interval)


def get_coordinates_list(
        gene_mapping_coords_str: str,
) -> list[P.Interval]:
    coords_list = [
        P.open(int(start), int(end))
        for start, end in (
            pair.strip('()').split(',')
            for pair in gene_mapping_coords_str.split('_')
        )
    ]
    return sorted(coords_list, key=lambda x: (x.lower, x.upper))


def get_p_coordinates_list_union(
    p_coordinates_list: list[P.Interval],
) -> P.Interval:
    return reduce(lambda x, y: x.union(y), p_coordinates_list)


